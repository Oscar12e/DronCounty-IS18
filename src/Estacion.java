public class Estacion {

    private int cantDrones;
    private int numeroEstacion;

    public Estacion(int cantDrones, int numeroEstacion) {
        this.cantDrones = cantDrones;
        this.numeroEstacion = numeroEstacion;
    }

    public int getCantDrones() {
        return cantDrones;
    }

    public int getNumeroEstacion() {
        return numeroEstacion;
    }
}
