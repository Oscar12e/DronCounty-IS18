public class Drone {

    private int largo;
    private int ancho;
    private int altura;
    private int velocidad;

    public Drone() {
        this.largo = 2;
        this.ancho = 2;
        this.altura = 3;
        this.velocidad = 120;
    }

    public int getLargo() {
        return largo;
    }

    public int getAncho() {
        return ancho;
    }

    public int getAltura() {
        return altura;
    }

    public int getVelocidad() {
        return velocidad;
    }
}
